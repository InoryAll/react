package com.entity;
/*教师实体类*/
public class Teacher {
	private String teaId;
	private String teaPassword;
	private String teaName;
	private String teaSex;
	private String teaNation;
	private int teaAge;
	private String teaDepartment;
	private String teaTel;
	private String teaMark;
	
	public String getTeaId() {
		return teaId;
	}
	public void setTeaId(String teaId) {
		this.teaId = teaId;
	}
	public String getTeaPassword() {
		return teaPassword;
	}
	public void setTeaPassword(String teaPassword) {
		this.teaPassword = teaPassword;
	}
	public String getTeaName() {
		return teaName;
	}
	public void setTeaName(String teaName) {
		this.teaName = teaName;
	}
	public String getTeaSex() {
		return teaSex;
	}
	public void setTeaSex(String teaSex) {
		this.teaSex = teaSex;
	}
	public String getTeaNation() {
		return teaNation;
	}
	public void setTeaNation(String teaNation) {
		this.teaNation = teaNation;
	}
	public int getTeaAge() {
		return teaAge;
	}
	public void setTeaAge(int teaAge) {
		this.teaAge = teaAge;
	}
	public String getTeaDepartment() {
		return teaDepartment;
	}
	public void setTeaDepartment(String teaDepartment) {
		this.teaDepartment = teaDepartment;
	}
	public String getTeaTel() {
		return teaTel;
	}
	public void setTeaTel(String teaTel) {
		this.teaTel = teaTel;
	}
	public String getTeaMark() {
		return teaMark;
	}
	public void setTeaMark(String teaMark) {
		this.teaMark = teaMark;
	}
	
	
}
