package com.entity;
/*学生实体类*/
public class Student {
	private String stuId;
	private String stuPassword;
	private String stuName;
	private String stuSex;
	private String stuNation;
	private int stuAge;
	private String stuDepartment;
	private String stuClass;
	private String stuTel;
	private String stuMark;
	
	public String getStuId() {
		return stuId;
	}
	public void setStuId(String stuId) {
		this.stuId = stuId;
	}
	public String getStuPassword() {
		return stuPassword;
	}
	public void setStuPassword(String stuPassword) {
		this.stuPassword = stuPassword;
	}
	public String getStuName() {
		return stuName;
	}
	public void setStuName(String stuName) {
		this.stuName = stuName;
	}
	public String getStuSex() {
		return stuSex;
	}
	public void setStuSex(String stuSex) {
		this.stuSex = stuSex;
	}
	public String getStuNation() {
		return stuNation;
	}
	public void setStuNation(String stuNation) {
		this.stuNation = stuNation;
	}
	public int getStuAge() {
		return stuAge;
	}
	public void setStuAge(int stuAge) {
		this.stuAge = stuAge;
	}
	public String getStuDepartment() {
		return stuDepartment;
	}
	public void setStuDepartment(String stuDepartment) {
		this.stuDepartment = stuDepartment;
	}
	public String getStuClass() {
		return stuClass;
	}
	public void setStuClass(String stuClass) {
		this.stuClass = stuClass;
	}
	public String getStuTel() {
		return stuTel;
	}
	public void setStuTel(String stuTel) {
		this.stuTel = stuTel;
	}
	public String getStuMark() {
		return stuMark;
	}
	public void setStuMark(String stuMark) {
		this.stuMark = stuMark;
	}
	
}
